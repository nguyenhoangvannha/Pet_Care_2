    public void loaddata() {
        Connection conn = null;
        Statement stt = null;
        ResultSet rs = null;
        dtmnv.setRowCount(0);
        // trươc khi ket noi fail add thu vien jdbc42.rar
        try {
            conn = MyMSSQLControl.getConnect();
            if (conn == null) {
                JOptionPane.showMessageDialog(rootPane, "lỗi kết nối");
            }
            stt = conn.createStatement();
            String sql = "select * from NHANVIEN";
            rs = stt.executeQuery(sql);
            ResultSetMetaData md = rs.getMetaData();
            Vector row;
            Vector colum = new Vector();
            int number = md.getColumnCount();
            // LAY TEN TUNG COLLUMN name (ROW 1)
            for (int i = 1; i <= number; i++) {
                colum.add(md.getColumnName(i));
                //System.out.println(md.getColumnName(i));
                dtmnv.setColumnIdentifiers(colum);

            }
            ;
            tblNhanVien.setModel(dtmnv);

            // LEN DU LIEU TUNG ROW
            while (rs.next()) {
                row = new Vector();
                //System.out.println("MACD: "+ rs.getString(1)+ " TENCD: "+rs.getString(2));
                row.addElement(rs.getString(1));
                row.addElement(rs.getString(2));
                row.addElement(rs.getString(3));
                row.addElement(rs.getString(4));
                row.addElement(rs.getString(5));
                row.addElement(rs.getString(6));
                row.addElement(rs.getString(7));
                row.addElement(rs.getString(8));
                row.addElement(rs.getBoolean(9));
                row.addElement(rs.getInt(10));
                row.addElement(rs.getString(11));
                dtmnv.addRow(row);

            }

            tblNhanVien.setModel(dtmnv);
            customTableUI();
            tblNhanVien.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {  // DUA DU LIEU VO 2 O TEXT FIELD TUONG UNG
                    if (tblNhanVien.getSelectedRow() >= 0) {
                        String manv = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 0).toString();
                        String tennv = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 1).toString();
                        String gtinh = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 2).toString();
                        String diachi = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 3).toString();
                        String sdt = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 4).toString();
                        String email = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 5).toString();
                        String cmnd = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 6).toString();
                        String chucvu = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 7).toString();
                        boolean nmk = Boolean.parseBoolean(tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 8).toString());
                        int mcv = Integer.parseInt(tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 9).toString());
                        String pw = tblNhanVien.getValueAt(tblNhanVien.getSelectedRow(), 10).toString();
                        nv = new NhanVien(manv, tennv, gtinh, diachi, sdt, email, cmnd, chucvu, nmk, mcv, pw);
                    }

                }

            });

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "loi ket nối");

        }
    }
