    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // tim kiếm

        boolean kt = false;
        for (int i = 0; i < tblNhanVien.getRowCount(); i++) {
            for (int j = 0; j < tblNhanVien.getColumnCount(); j++) {
                kt = true;
                if (txtSearch.getText().equals(tblNhanVien.getValueAt(i, j).toString())) {
                    tblNhanVien.setCellSelectionEnabled(true);
                    tblNhanVien.changeSelection(i, j, false, false);
                    tblNhanVien.requestFocus();
                }
            }
        }
        if (kt == false) {
            JOptionPane.showMessageDialog(this, "Không có thông tin nhân viên cần tìm");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

