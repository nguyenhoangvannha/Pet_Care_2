    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // tim kiếm

        boolean kt = false;
        for (int i = 0; i < jttt.getRowCount(); i++) {
            for (int j = 0; j < jttt.getColumnCount(); j++) {
                kt = true;
                if (txtSearch.getText().equals(jttt.getValueAt(i, j).toString())) {
                    jttt.setCellSelectionEnabled(true);
                    jttt.changeSelection(i, j, false, false);
                    jttt.requestFocus();
                }
            }
        }
        if (kt == false) {
            JOptionPane.showMessageDialog(this, "Không có thông tin  cần tìm");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

