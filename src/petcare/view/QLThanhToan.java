    public void loaddata() {
        Connection conn = null;
        Statement stt = null;
        ResultSet rs = null;
        dtmtt.setRowCount(0);
        // trươc khi ket noi fail add thu vien jdbc42.rar
        try {
            conn = MyMSSQLControl.getConnect();
            if (conn == null) {
                JOptionPane.showMessageDialog(rootPane, "lỗi kết nối");
            }
            stt = conn.createStatement();
            String sql = "select * from THANHTOAN";
            rs = stt.executeQuery(sql);
            ResultSetMetaData md = rs.getMetaData();
            Vector row;
            Vector colum = new Vector();
            int number = md.getColumnCount();
            // LAY TEN TUNG COLLUMN name (ROW 1)
            for (int i = 1; i <= number; i++) {
                colum.add(md.getColumnName(i));
                //System.out.println(md.getColumnName(i));
                dtmtt.setColumnIdentifiers(colum);

            }

            jttt.setModel(dtmtt);

            // LEN DU LIEU TUNG ROW
            while (rs.next()) {
                row = new Vector();
                //System.out.println("MACD: "+ rs.getString(1)+ " TENCD: "+rs.getString(2));
                row.addElement(rs.getString(1));

                row.addElement(rs.getString(2));
                row.addElement(rs.getString(3));
                row.addElement(rs.getDate(4));
                row.addElement(rs.getFloat(5));

                dtmtt.addRow(row);

            }

            jttt.setModel(dtmtt);
            jttt.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {  // DUA DU LIEU VO 2 O TEXT FIELD TUONG UNG
                    if (jttt.getSelectedRow() >= 0) {
                        String matt = jttt.getValueAt(jttt.getSelectedRow(), 0).toString();

                        String mayt = jttt.getValueAt(jttt.getSelectedRow(), 1).toString();
                        String makh = jttt.getValueAt(jttt.getSelectedRow(), 2).toString();
                        Date ntt = Date.valueOf(jttt.getValueAt(jttt.getSelectedRow(), 3).toString());
                        float ttien = Float.valueOf(jttt.getValueAt(jttt.getSelectedRow(), 4).toString());

                        tt = new ThanhToan(matt, mayt, makh, ntt, ttien);
                    }

                }

            });

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "loi ket nối");

        }
    }
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //xoa khach hang
        Connection conn = null;
        try {
            conn = MyMSSQLControl.getConnect();
            if (conn == null) {
                JOptionPane.showMessageDialog(rootPane, "lỗi kết nối");
            }
            String sql = "delete THANHTOAN where MATT=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jttt.getValueAt(jttt.getSelectedRow(), 0).toString());
            int a = ps.executeUpdate();
            if (a > 0) {

                dtmtt.setRowCount(0);
                loaddata();
            }

        } catch (Exception e) {
            // JOptionPane.showMessageDialog(rootPane, "Xóa không thành công");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

