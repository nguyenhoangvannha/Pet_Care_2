    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // tim kiếm

        boolean kt= false;
        for(int i=0;i<tblThuoc.getRowCount();i++)
        {
            for(int j=0;j<tblThuoc.getColumnCount();j++)
            {
                kt=true;
                if(txtSearch.getText().equals(tblThuoc.getValueAt(i, j).toString()))
                {
                    tblThuoc.setCellSelectionEnabled(true);
                    tblThuoc.changeSelection(i,j, false, false);
                    tblThuoc.requestFocus();
                }
            }
        }
        if(kt==false) JOptionPane.showMessageDialog(this, "Không có thông tin thuốc cần tìm");
    }//GEN-LAST:event_btnSearchActionPerformed

