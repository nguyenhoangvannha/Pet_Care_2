    public void loaddata()
    {
        Connection conn =null;
        Statement stt=null;
        ResultSet rs= null;
        dtmt.setRowCount(0);
        try
        {
            conn=MyMSSQLControl.getConnect();
            if(conn==null)
            {
                JOptionPane.showMessageDialog(rootPane, "lỗi kết nối");
            }
            stt=conn.createStatement();
            String sql = "select * from THUOC";
            rs=stt.executeQuery(sql);
            ResultSetMetaData md = rs.getMetaData();
            Vector row;
            Vector colum= new Vector();
            int number = md.getColumnCount();
             // LAY TEN TUNG COLLUMN name (ROW 1)
            for (int i=1;i<=number;i++)
            {
                colum.add(md.getColumnName(i));
                //System.out.println(md.getColumnName(i));
                dtmt.setColumnIdentifiers(colum);
                
            }
            
            tblThuoc.setModel(dtmt);

             // LEN DU LIEU TUNG ROW
            while(rs.next())
            {
                row= new Vector();
                 //System.out.println("MACD: "+ rs.getString(1)+ " TENCD: "+rs.getString(2));
                row.addElement(rs.getString(1));
                row.addElement(rs.getString(2));
                row.addElement(rs.getFloat(3));
                row.addElement(rs.getDate(4));
                row.addElement(rs.getString(5));
                dtmt.addRow(row);
                
            }
            
            tblThuoc.setModel(dtmt);
            customTableUI();
            tblThuoc.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {  // DUA DU LIEU VO 2 O TEXT FIELD TUONG UNG
                   if(tblThuoc.getSelectedRow()>=0)
                   {
                       String mat = tblThuoc.getValueAt(tblThuoc.getSelectedRow(), 0).toString();
                       String tent = tblThuoc.getValueAt(tblThuoc.getSelectedRow(), 1).toString();
                       float gia = Float.parseFloat(tblThuoc.getValueAt(tblThuoc.getSelectedRow(), 2).toString());
                       Date hsd = Date.valueOf(tblThuoc.getValueAt(tblThuoc.getSelectedRow(), 3).toString());
                       String cdung = tblThuoc.getValueAt(tblThuoc.getSelectedRow(), 4).toString();
                       
                       th = new Thuoc(mat,tent,gia,hsd,cdung);
                   }
                       
           
                }
              
        });
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "loi ket nối");
            
        }
    }   
